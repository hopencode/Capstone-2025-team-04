#!/bin/bash

# Enhanced MLP 사기 탐지 시스템 전체 파이프라인 실행 스크립트

# 스크립트 실행 중 오류가 발생하면 즉시 중단
set -e

echo "==============================================="
echo "🚀 MLP 사기 탐지 파이프라인을 시작합니다."
